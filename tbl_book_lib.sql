
  CREATE TABLE "TBL_BOOK_LIB" 
   (	"BOOK_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"BOOK_NAME" VARCHAR2(1000 CHAR) NOT NULL ENABLE, 
	"BOOK_AUTIOR" VARCHAR2(1000 CHAR) NOT NULL ENABLE, 
	"BOOK_TRANSLATOR" VARCHAR2(1000 CHAR), 
	"BOOK_ISBN" NUMBER(13,0) NOT NULL ENABLE, 
	"BOOK_CATEGORY" VARCHAR2(100 CHAR) NOT NULL ENABLE, 
	"BOOK_AGE_USED" VARCHAR2(100 CHAR) NOT NULL ENABLE, 
	"BOOK_REGISTRATION_DATE" DATE NOT NULL ENABLE, 
	 CONSTRAINT "TBL_BOOK_LIB_PK" PRIMARY KEY ("BOOK_ID")
  USING INDEX  ENABLE, 
	 CONSTRAINT "TBL_BOOK_LIB_CON_UNIQ" UNIQUE ("BOOK_ISBN")
  USING INDEX  ENABLE
   ) ;

  CREATE OR REPLACE EDITIONABLE TRIGGER "TBL_BOOK_LIB_TRG" 
BEFORE INSERT OR UPDATE OR DELETE ON "TBL_BOOK_LIB"
FOR EACH ROW
BEGIN
  IF INSERTING THEN
    SELECT SEQ_LIBRARY.NEXTVAL
    INTO :NEW.BOOK_ID
    from TBL_BOOK_LIB;
  END IF;
END;
/
ALTER TRIGGER "TBL_BOOK_LIB_TRG" ENABLE;